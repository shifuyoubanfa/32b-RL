def build_pool_anchor(v1_answers) -> str:
    """把 V1 答案池(v1_answers)抽出的 极性/数字/日期 拼成结构化锚点 —— 正是 answer_in_v1_pool 的覆盖靶子。
    空池返回 ""（调用方回退到原答案 prose）。"""
    from pipeline.rules_v6 import _extract_facts
    pol, val, date = set(), set(), set()
    for a in v1_answers or []:
        e = _extract_facts(a)
        pol |= e["pol"]
        val |= e["val"]
        date |= e["date"]
    parts = []
    if pol:
        parts.append("结论方向（只能落在这些极性上、不得相反）：" + "、".join(sorted(pol)))
    if val:
        parts.append("可用的关键数字/金额/比例（只能用这些、不得新增）：" + "、".join(sorted(val)))
    if date:
        parts.append("涉及日期/期限：" + "、".join(sorted(date)))
    return "\n".join(parts)
